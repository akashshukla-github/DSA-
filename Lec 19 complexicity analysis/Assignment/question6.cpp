#include <iostream>
#include <algorithm>
#include <string>
#include <vector>

using namespace std;

int main()
{
    int c = 0;
    int n;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < i; j++)
        {
            c++;
        }
    }

    return 0;
}
